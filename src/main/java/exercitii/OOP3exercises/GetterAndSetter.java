package exercitii.OOP3exercises;

public class GetterAndSetter {// using "this."
	private int x;

	public GetterAndSetter(int x) {
		this.x = x;
	}

	public int getX() {
		return this.x;
	}

	public void setX(int x) {
		this.x = x;
	}

	private boolean y;

	public boolean isY() {
		return this.y;

	}

	public void setY(boolean y) {
		this.y = y;
	}
}
